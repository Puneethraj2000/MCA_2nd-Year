#Word and Character count of a given string

string = " Vivekananda College of"
print("The number of characters in the string is:",len(string))
char = string.split()
print("The number of words in the string is:",len(char))
total = 0
for i in char:
	val = len(i)
	total+=val
print("The number of characters in the string without space is:",total)
print("The number of characters in the string without space is:",len(string.strip())-len(char)+1)
