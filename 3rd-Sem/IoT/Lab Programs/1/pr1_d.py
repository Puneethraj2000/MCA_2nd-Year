#Area of given shapes (Rectangle, Triangle and Shape) reading shape and appropriate values from standard input

def rect():
	l = float(input("Enter length:"))
	b = float(input("Enter breadth:"))
	rect = l * b
	print("Area of Rectangle:",rect)
def tri():
	base = float(input("Enter Base:"))
	h = float(input("Enter Height:"))
	tri = (base * h) / 2
	print("Area of Triangle:",tri)
def cir():
	r = float(input("Enter radius:"))
	cir = 3.14 * r * r
	print("Area of Circle:",cir)
	
while(True):
	c = int(input("1.Rectangle\n2.Triangle\n3.Circle\n4.Exit\nEnter your choice:\n"))
	if c == 1:
		rect()
	elif c == 2:
		tri()
	elif c == 3:
		cir()
	elif c == 4:
		quit()
	else:
		print("Invalid Choice")
